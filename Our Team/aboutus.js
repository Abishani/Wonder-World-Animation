// Student One
function hover1() {
  document.getElementById("details").innerHTML = "	Stephaniya Joseph- Student 1";       //shows name of student 1 on mouseover
  pic2.style.opacity = "0.4";
  pic3.style.opacity = "0.4";
  pic4.style.opacity = "0.4";
}

function mouseOut1() {
  document.getElementById("details").innerHTML = "Hover on photo to see details";    //shows instruction on mouseout
  pic2.style.opacity = "1";
  pic3.style.opacity = "1";
  pic4.style.opacity = "1";
}

